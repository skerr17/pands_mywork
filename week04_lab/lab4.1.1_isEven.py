# Asks the user for a number and checks if it is odd or even
# Author: Stephen Kerr

# Prompt the user for a number
number = int(input("Please enter a number: "))

# Check if the number is even or odd using an If, Else Statemets
# Prints the output

if (number % 2) == 0:
    print (f'{number} is an even number') 
else:
    print(f'{number} is an odd number')
